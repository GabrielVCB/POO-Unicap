public class Medico {
    int codigo;
    String nome;
    char sexo;
    String especialidade;
    String endereco;

    public Medico(int codigo,String nome,char sexo,String especialidade,String endereco) {
        this.codigo = codigo;
        this.nome = nome;
        this.sexo = sexo;
        this.especialidade = especialidade;
        this.endereco = endereco;
    }
}
